package readers

import "time"

type DateInfo string

func ReadDate(format string) func() (interface{}, error) {
	return func() (interface{}, error) {
		date := time.Now()

		// Reference: https://gosamples.dev/date-time-format-cheatsheet/
		return date.Format(format), nil
	}
}
